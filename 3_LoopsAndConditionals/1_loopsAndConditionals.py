# If-Else Statement
num = 10
if num > 0:
    print("Positive number")
else:
    print("Non-positive number")

# For Loop
for i in range(5):
    print("For Loop:", i)

# While Loop
count = 0
while count < 5:
    print("While Loop:", count)
    count += 1

# Nested Loops
for i in range(3):
    for j in range(2):
        print(f"i = {i}, j = {j}")
